public class Count {
    /**
     * Сложение двух чисел
     * @param x - первое число типа int
     * @param y - второе число типа int
     * @return - вернет сумму двух чисел x и y
     */
    public int add(int x , int y) {
        return x + y;
    }
    /**
     * Умножение двух чисел
     * @param x - первое число типа int
     * @param y - второе число типа int
     * @return - вернет сумму двух чисел x и y
     */
    public int multiply(int x , int y) {
        return x * y;
    }
}
